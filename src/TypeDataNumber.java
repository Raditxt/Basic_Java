public class TypeDataNumber {
    public static void main(String[] args) {
        // Tipe data bilangan bulat
        byte exampleByte = 100;
        short exampleShort = 1000;
        int exampleInt = 100_000_000;
        long exampleLong1 = 1_000_000_000L;
        long exampleLong2 = 100_000_000_000L;

        // Floating point
        float exampleFloat = 1.5F;
        double exampleDouble = 12.2404;

        // Menampilkan hasil
        System.out.println("Byte value: " + exampleByte);
        System.out.println("Short value: " + exampleShort);
        System.out.println("Int value: " + exampleInt);
        System.out.println("Long value 1: " + exampleLong1);
        System.out.println("Long value 2: " + exampleLong2);
        System.out.println("Float value: " + exampleFloat);
        System.out.println("Double value: " + exampleDouble);
    }
}
